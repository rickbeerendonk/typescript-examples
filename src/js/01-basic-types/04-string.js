// Single quotes
var a = 'one';
console.log('a:', a); // one
console.log('typeof(a):', typeof a); // string

// Double quotes
var b = 'two';
console.log('b:', b); // two
console.log('typeof(b):', typeof b); // string

// One character
var c = 'c';
console.log('c:', c); // c
console.log('typeof(c):', typeof c); // string
